# python program to find volume of a sphere
radius = float (input("enter the value of radius of sphere: "))

def volume (r):
    v = (4.0/3.0) * 3.14 * r **3
    return v

required_volume_of_sphere = volume (radius)
print (required_volume_of_sphere)


